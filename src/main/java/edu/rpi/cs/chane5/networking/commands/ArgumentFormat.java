package edu.rpi.cs.chane5.networking.commands;

/**
 * Enum type for labeling valid command argument type.
 *
 * @version {@value Command#VERSION}
 */
public enum ArgumentFormat {
    /**
     * String type argument.
     */
    STRING,
    /**
     * Integer type argument.
     */
    INT,
    /**
     * Double type argument.
     */
    DOUBLE,
    /**
     * Boolean type argument.
     */
    BOOL,
    /**
     * Character type argument.
     */
    CHAR
}
